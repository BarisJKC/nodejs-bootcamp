/**
 * Add two numbers
 * @param {*} x 
 * @param {*} y 
 */
const add = (x, y) => x + y;

/**
 * Subtract two numbers
 * @param {*} x 
 * @param {*} y 
 */
const subtract = (x, y) => x - y;

/**
 * Divide two numbers
 * @param {*} x 
 * @param {*} y 
 */
const divide = (x, y) => x / y;

/**
 * Multiplication of two numbers
 * @param {*} x 
 * @param {*} y 
 */
const multiply = (x, y) => x * y;

module.exports = {
  add,
  subtract,
  divide,
  multiply
};
